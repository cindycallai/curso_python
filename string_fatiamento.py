nome = "Cindy Callai Braun"

# exibe a primeira posição
print(nome[0])
print(nome[-1]) # última letra

# exibe do início até o caracter 5
print(nome[:5])

# exibe a partir do caracter 5 até o final
print(nome[5:])

# exibe do caracter 5 até o 10
print(nome[5:10])

# exibe de 5 a 10 pulando de duas em duas letras
print(nome[5:10:2])

# exibe todo o texto
print(nome[:])

# exibe o texto ao contrário/espelhamento
print(nome[::-1])