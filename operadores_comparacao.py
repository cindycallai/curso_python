# São operadores utilizados para comparar dois valores.

# igualdade
saldo = 450
saque = 200
print(saldo == saque)

# diferença
saldo = 450
saque = 200
print(saldo  != saque)

# Maior
saldo = 450
saque = 200
print(saldo > saque)

# Maior ou igual
saldo = 450
saque = 200
print(saldo  >= saque)

# Menor
saldo = 450
saque = 200
print(saldo < saque)

# Menor ou igual
saldo = 450
saque = 200
print(saldo  <= saque)