# São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável.

# Atribuição simples
saldo = 50
print(saldo)

# Atribuição com adição
saldo = 500
saldo += 200 #soma o valor da variável com a atribuição 500 + 200 = 700
print(saldo)
# Atribuição com subtração
saldo = 300
saldo -= 200
print(saldo)

# Atribuição com multiplicação
saldo = 100
saldo *= 200
print(saldo)

# Atribuição com divisão
saldo = 200
saldo /= 2
print(saldo)

# Atribuição com divisão inteira
saldo = 100
saldo //= 5
print(saldo)

# Atribuição com módulo
saldo = 100
saldo %= 7
print(saldo)

# Atribuição com exponenciação
saldo = 100
saldo **= 5
print(saldo)

