# Adição
print(1 + 1)

# Subtração
print(10 - 2)

# Multiplicação
print(4 * 3)

# Divisão
print(12 / 3)
# Divisão inteira - mostra apenas a parte inteira
print(12 // 3)

# Módulo - resto da divisão
print(2 % 3)

# Exponenciação - valor elevado
print(2 ** 3)

# Precedência dos operadores: 
# 1º Parêntesis
# 2º Expoêntes
# 3º Multiplicações e divisões (da esquerda para a direita)
# 4º Somas e subtrações (da esquerda para a direita)

print(10 - 5 * 2)
print((10 - 5) * 2)
print(10 ** 2 * 2)
print(10 ** (2 * 2))
print(10 / 2 * 4)

